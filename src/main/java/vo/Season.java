package vo;

public class Season {
	private Integer gameday;
	private String season;

	public Integer getGameday() {
		return gameday;
	}

	public void setGameday(Integer gameday) {
		this.gameday = gameday;
	}

	public String getSeason() {
		return season;
	}

	public void setSeason(String season) {
		this.season = season;
	}

}
